    private void okBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okBtnActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(oldpwd, sb, "Thiếu mật khẩu cũ");
        DataValidator.validateEmpty(newpwd, sb, "Thiếu mật khẩu mới");
        DataValidator.validateEmpty(confnewpwd, sb, "Thiếu xác nhận mật khẩu mới");
        String oldpassword = new String (oldpwd.getPassword());
        String password = new String(newpwd.getPassword());
        String confpassword = new String (confnewpwd.getPassword());
        if ((!password.equals(""))&&(!confpassword.equals(password))){
            sb.append("Mật khẩu không trùng khớp! \n");
            newpwd.setBackground(Color.yellow);
            confnewpwd.setBackground(Color.yellow);
            return;
        }
        if(sb.length()>0){
            MessageHelper.ErrorMesage(this, sb.toString(), "ERROR");
        }
        KhachHangDao dao=new KhachHangDao();
        try {
            boolean check = dao.IsUser(khachhang.getUsername(),oldpassword);
             if (check){
                if((MessageHelper.showConfirmMesage(this, "Xác nhận đổi mật khẩu", "Okay"))==0) {
                    String salt = BCrypt.gensalt(4);
                    String hash = BCrypt.hashpw(password, salt);
                    khachhang.setPassword(hash);
                    if(dao.changePwd(khachhang)){
                        MessageHelper.showMesage(this, "Đã lưu thành công", "Thông báo");
                        this.dispose();
                    }
                    else{
                        MessageHelper.ErrorMesage(this, "Không lưu được do lỗi", "ERROR");
                    }
                }
            }
            else {
                 MessageHelper.showMesage(this, "Sai mật khẩu cũ", "Thông báo");    
            }
        } catch (Exception ex) {
            Logger.getLogger(changePwd.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }//GEN-LAST:event_okBtnActionPerformed

