    private void table_BillMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_BillMouseClicked
        try {
            int row=table_Bill.getSelectedRow();
            if (row>=0)
            {
                String id=(String) tblTableModel.getValueAt(row,0);
                HoaDonDao sb=new HoaDonDao();
                HoaDon hd =sb.FindHoaDon(id);
                KhachHangDao KH=new KhachHangDao();
                KhachHang kh1=KH.FindKhachHang(hd.getCMND());
                if (hd!=null) {
                    txt_cmnd.setText(hd.getCMND());
                    txt_mahd.setText(hd.getMaHD());
                    Format formatter = new SimpleDateFormat("yyyy-MM-dd");
                    String s = formatter.format(hd.getNgayLap());
                    txt_ngaydatve.setText(s);
                    txt_hoten.setText(kh1.getTENKH());
                    txt_sdt.setText(kh1.getSDT());
                    if (kh1.getGIOITINH()==1)
                    {
                        txt_gioitinh.setText("Nam");
                    }
                    else txt_gioitinh.setText("Nữ");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            MessageHelper.ErrorMesage(parentForm,"Lỗi","Lỗi");
        }
    }//GEN-LAST:event_table_BillMouseClicked

