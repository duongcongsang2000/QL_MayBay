    public void LoadTable(){
        try {
            HoaDonDao HD=new HoaDonDao();
            KhachHangDao KH=new KhachHangDao();
            List<HoaDon> list=HD.findallHoaDon();
            tblTableModel.setRowCount(0);
           for(HoaDon sb :list)
           {
               String gioitinh="";
               KhachHang KH1=KH.FindKhachHang(sb.getCMND());
               if (KH1.getGIOITINH()==1) gioitinh="Nam";
                       else gioitinh="Nữ";
               tblTableModel.addRow(new Object[]{
                   sb.getMaHD(),sb.getCMND(),KH1.getTENKH(),KH1.getSDT(),gioitinh,sb.getNgayLap()});
           tblTableModel.fireTableDataChanged(); }
           }
        catch (Exception e) {
        }
    }
     public void LoadTable3(Date Ngay){
        try {
            HoaDonDao HD=new HoaDonDao();
            KhachHangDao KH=new KhachHangDao();
            List<HoaDon> list=HD.findallHoaDon();
            tblTableModel.setRowCount(0);
            String date1=stf.format(Ngay);
           for(HoaDon sb :list)
           {
               String date2=stf.format(sb.getNgayLap());
               if (date1.equals(date2))
                {       
                    String gioitinh="";
                    KhachHang KH1=KH.FindKhachHang(sb.getCMND());
                    if (KH1.getGIOITINH()==1) gioitinh="Nam";
                       else gioitinh="Nữ";
                    tblTableModel.addRow(new Object[]{
                    sb.getMaHD(),sb.getCMND(),KH1.getTENKH(),KH1.getSDT(),gioitinh,sb.getNgayLap()});
                }
                tblTableModel.fireTableDataChanged(); }
           }
        catch (Exception e) {
        }
    }
