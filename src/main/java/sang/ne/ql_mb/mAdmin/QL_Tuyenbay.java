    private void table_tuyenbayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_tuyenbayMouseClicked
        try {
            int row=table_tuyenbay.getSelectedRow();
            if (row>=0) 
            {
                String id=(String) tblTableModel.getValueAt(row,0);
                TuyenBayDao sb=new TuyenBayDao();
                TuyenBay tuyenb =sb.findTuyenBay(id);
                if (tuyenb!=null) {
                    txt_matuyenbay.setText(tuyenb.getMaTuyenBay());
                    txt_noikhoihanh.setText(tuyenb.getSanBayDi());
                    txt_diemden.setText(tuyenb.getSanBayDen());
                    int x=tuyenb.getChieudai();
                    txt_chieudai.setText(String.valueOf(x));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
             MessageHelper.ErrorMesage(parentForm,"Lỗi","Lỗi");
        }
    }//GEN-LAST:event_table_tuyenbayMouseClicked

