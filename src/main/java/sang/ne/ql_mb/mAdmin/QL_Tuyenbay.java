    public void loadData(){
        try{
            TuyenBayDao tuyenbay = new TuyenBayDao();
            SanBayDao sanbay=new SanBayDao();
            List<TuyenBay> list = tuyenbay.findalltuyenBay();
            tblTableModel.setRowCount(0);
            for (TuyenBay cb : list){
               SanBay sb1=sanbay.FindTenSanBay(cb.getSanBayDi());
               SanBay sb2=sanbay.FindTenSanBay(cb.getSanBayDen());
               tblTableModel.addRow(new Object[]{
               cb.getMaTuyenBay(),cb.getSanBayDi(),sb1.getTenSanBay(),cb.getSanBayDen(),sb2.getTenSanBay(),cb.getChieudai()
               });
                
            }
            
            tblTableModel.fireTableDataChanged();
            
        } catch (Exception e) {
            
        }
    }
