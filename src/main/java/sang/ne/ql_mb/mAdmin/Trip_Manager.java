     public void LoadTable(){
         try {
            ChuyenBayDao chuyenbay=new ChuyenBayDao();
            List < ChuyenBay > list=chuyenbay.findChuyenBay();
            mdDefaultTableModel.setRowCount(0);
           for(ChuyenBay sb :list)
               mdDefaultTableModel.addRow(new Object[]{
                   sb.getMaChuyenBay(),sb.getMaTuyenBay(),sb.getNgayGio(),sb.getThoiGianBay(),sb.getSoGheTrong(),
                   sb.getMaChiTietChuyenBay()});
           mdDefaultTableModel.fireTableDataChanged();
        } catch (Exception e) {
        }
     }
