    private void table_tripMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_tripMouseClicked
         try {
            int row=table_trip.getSelectedRow();
            if (row>=0) 
            {
                String id=(String) mdDefaultTableModel.getValueAt(row,0);
                ChuyenBayDao sb=new ChuyenBayDao();
                ChuyenBay chuyenb =sb.find1ChuyenBay(id);
                if (chuyenb!=null) {
                    machuyenbay.setText(chuyenb.getMaChuyenBay());
                    matuyenbay.setText(chuyenb.getMaTuyenBay());
                    machitiet.setText(chuyenb.getMaChiTietChuyenBay());
                    Format formatter = new SimpleDateFormat("yyyy-MM-dd");
                    String s = formatter.format(chuyenb.getNgayGio());
                    ngaybay.setText(s);
                   // Format formatt = new SimpleDateFormat("HH:mm:ss");
                    //String ss=formatt.format(chuyenb.getThoiGianBay());
                   // thoigianbay.setText(ss);
                   thoigianbay.setText(chuyenb.getThoiGianBay());
                    int tg=chuyenb.getSoGheTrong();
                    soghe.setText(String.valueOf(tg));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
             MessageHelper.ErrorMesage(parentForm,"Lỗi","Lỗi");
        }
    }//GEN-LAST:event_table_tripMouseClicked

